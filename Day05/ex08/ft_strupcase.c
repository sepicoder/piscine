char	*ft_strupcase(char *str)
{
	int index;

	index = 0;
	while (str[index] != '\0')
	{
		if (str[index] <= 'z' && str[index] >= 'a')
		{
			str[index] -= 32;
		}
		index++;
	}
	return (str);
}
