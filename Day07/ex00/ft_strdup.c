#include <stdlib.h>

char	*ft_strdup(char *src)
{
	int		i;
	char	*copy;
	int		length;

	length = 0;
	while (src[length] != '\0')
	{
		length++;
	}
	copy = (char*)malloc(sizeof(*src) * (length + 1));
	i = 0;
	while (src[i] != '\0')
	{
		copy[i] = src[i];
		i++;
	}
	copy[i] = '\0';
	return (copy);
}
